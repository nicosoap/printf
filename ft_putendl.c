#include <unistd.h>
#include "libftprintf.h"

void		ft_putendl(char const *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		write(1, s + i, 1);
		i++;
	}
	ft_putchar('\n');
}
