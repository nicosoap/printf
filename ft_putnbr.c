#include "libftprintf.h"

void		ft_putnbr(int n)
{
	char	a;

	if (n < 0)
		ft_putchar('-');
	if (n / 10)
		ft_putnbr(ft_abs(n / 10));
	a = '0' + ft_abs(n % 10);
	ft_putchar(a);
}
