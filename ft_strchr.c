#include <string.h>

char		*ft_strchr(const char *s, int c)
{
	char *ptr;

	ptr = (char *)s;
	while (*ptr != c && *ptr != '\0')
		ptr++;
	if (*ptr == '\0')
		if (c != '\0')
			return (NULL);
	return (ptr);
}
