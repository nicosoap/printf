void		ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		i;
	char	*ptr;

	ptr = s;
	i = 0;
	if (*f && s)
	{
		while (*ptr != '\0')
		{
			f(i, ptr);
			i++;
			ptr++;
		}
	}
}
